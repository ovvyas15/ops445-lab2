# Initialize the timer with the value 10
timer = 10

# Loop until the timer reaches 0
while timer > 0:
    print(timer)  # Print the current value of the timer
    timer -= 1  # Decrease the timer by 1

# Print 'blast off!' once the loop ends
print('blast off!')

